    public void rellenar_tablarestaurantes(ArrayList<Restaurante> restaurantes){
        
        
        DefaultTableModel model = (DefaultTableModel) tabla_user.getModel();
        
        model.getDataVector().removeAllElements();
        revalidate();
        
        for (int i = 0; i < restaurantes.size(); i++) {
           
            model.insertRow(model.getRowCount(), new Object[]{
                restaurantes.get(i).getNombre(),
                restaurantes.get(i).getTipoComida()});
        
        }
    
    }
    public void insertar_platos(ArrayList<Plato> platos){
        this.platos = platos;
        
        DefaultTableModel model = (DefaultTableModel) jtplatos.getModel();
        jLabel2.setIcon(null);
         model.getDataVector().removeAllElements();
        revalidate();
        for (int i = 0; i < platos.size(); i++) {
           
            model.insertRow(model.getRowCount(), new Object[]{
                platos.get(i).getNombre(),
                platos.get(i).getPrecio()});
        
        }
    }
    public void eliminar_tablarestaurantes(int remove){
        
        
        DefaultTableModel model = (DefaultTableModel) tabla_user.getModel();
        model.removeRow(remove);
            
            
        
       
    
    }

